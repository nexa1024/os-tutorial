; ============================================
; 03-引导内存 - 使用 org 指令简化地址处理
; ============================================
; [org 0x7c00] 告诉汇编器：代码会被加载到 0x7c00
; 这样所有标签地址都会自动加上这个偏移
; ============================================

[org 0x7c00]        ; 设置代码起始地址为 0x7c00

mov ah, 0x0e        ; BIOS int 0x10, ah=0x0e = 显示字符模式

; ============================================
; 尝试 1：直接获取标签（仍然错误）
; ============================================
; 问题：即使有 org，mov al, the_secret 还是获取地址
;       不是内容，所以仍然错误
; 结果：打印地址值而不是 'X'
mov al, '1'
int 0x10            ; 打印 '1'
mov al, the_secret  ; 获取地址（不是内容）
int 0x10            ; 打印地址值（错误）

; ============================================
; 尝试 2：间接寻址（现在正确了！）
; ============================================
; 正确：有了 org 后，[the_secret] 会自动使用正确的地址
;       the_secret 的地址 = 标签偏移 + 0x7c00
;       这是推荐的标准写法！
mov al, '2'
int 0x10            ; 打印 '2'
mov al, [the_secret]    ; 解引用获取内容（org 已处理偏移）
int 0x10            ; 打印 'X'（正确！）

; ============================================
; 尝试 3：重复加偏移（错误）
; ============================================
; 问题：org 已经自动加了 0x7c00，我们又手动加了一次
;       相当于加了两次偏移，地址错了
; 结果：访问到错误的内存位置
mov al, '3'
int 0x10            ; 打印 '3'
mov bx, the_secret  ; 获取地址（org 已加偏移）
add bx, 0x7c00      ; 又加了一次偏移（错误！）
mov al, [bx]        ; 解引用
int 0x10            ; 打印垃圾数据

; ============================================
; 尝试 4：直接硬编码地址（仍然正确但不实用）
; ============================================
; 正确：直接使用绝对地址，不受 org 影响
; 问题：仍然不推荐，因为每次修改代码都要重新计算
mov al, '4'
int 0x10            ; 打印 '4'
mov al, [0x7c2d]    ; 硬编码地址
int 0x10            ; 打印 'X'（正确但不推荐）

; ============================================
; 无限循环
; ============================================
jmp $               ; $ = 当前地址，形成死循环

; ============================================
; 数据段
; ============================================
the_secret:
    db "X"          ; 存储 'X' 字符（ASCII 0x58）

; ============================================
; 引导扇区尾部
; ============================================
; 填充 0 直到 510 字节，然后写入魔数 0xaa55
times 510-($-$$) db 0
dw 0xaa55
