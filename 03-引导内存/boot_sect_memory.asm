; ============================================
; 03-引导内存 - 内存寻址演示
; ============================================
; 本程序演示 4 种不同的内存访问方式
; 只有方式 3 和 4 能正确打印出 'X'
; ============================================

mov ah, 0x0e        ; BIOS int 0x10, ah=0x0e = 显示字符模式

; ============================================
; 尝试 1：直接获取标签（错误）
; ============================================
; 问题：mov al, the_secret 获取的是标签的地址（指针值）
;       而不是该地址处存储的内容
; 结果：会打印出地址值，而不是 'X'
mov al, '1'
int 0x10            ; 打印 '1'
mov al, the_secret  ; 获取 the_secret 的地址
int 0x10            ; 打印地址值（错误）

; ============================================
; 尝试 2：间接寻址但缺少偏移（错误）
; ============================================
; 问题：[the_secret] 会解引用获取内容，但地址不对
;       BIOS 把引导扇区加载到 0x7c00，而汇编器认为从 0 开始
;       所以需要加上 0x7c00 的偏移
; 结果：会打印出 0x0000 处的随机垃圾数据
mov al, '2'
int 0x10            ; 打印 '2'
mov al, [the_secret]    ; 尝试获取内容，但地址缺少 0x7c00 偏移
int 0x10            ; 打印垃圾数据（错误）

; ============================================
; 尝试 3：手动加上 0x7c00 偏移（正确！）
; ============================================
; 正确：先获取标签地址，加上 0x7c00 偏移，再解引用
;       这样就能访问到正确的内存位置
; 注意：不能用 [ax]，因为 x86 不允许同一指令中源和目标都是同一个寄存器
;       所以需要用 bx 作为中转
mov al, '3'
int 0x10            ; 打印 '3'
mov bx, the_secret  ; 获取标签地址到 bx
add bx, 0x7c00      ; 加上 BIOS 加载偏移
mov al, [bx]        ; 解引用获取内容
int 0x10            ; 打印 'X'（正确！）

; ============================================
; 尝试 4：直接硬编码地址（正确但不实用）
; ============================================
; 正确：直接使用 0x7c2d（the_secret 的实际地址）
; 问题：每次修改代码都要重新计算偏移量，非常不方便
;       用 xxd boot_sect_memory.bin 可以查看 'X' 在第几个字节
mov al, '4'
int 0x10            ; 打印 '4'
mov al, [0x7c2d]    ; 直接硬编码地址
int 0x10            ; 打印 'X'（正确但不推荐）

; ============================================
; 无限循环
; ============================================
jmp $               ; $ = 当前地址，形成死循环

; ============================================
; 数据段
; ============================================
the_secret:
    db "X"          ; 存储 'X' 字符（ASCII 0x58）

; ============================================
; 引导扇区尾部
; ============================================
; 填充 0 直到 510 字节，然后写入魔数 0xaa55
times 510-($-$$) db 0
dw 0xaa55
