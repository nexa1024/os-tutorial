; ============================================
; 引导扇区栈演示
; ============================================
; 功能: 演示栈的基本操作
; 编译: nasm -f bin boot_sect_stack.asm -o boot_sect_stack.bin
; 运行: qemu-system-x86_64 boot_sect_stack.bin
; 预期输出: A[CBA（A 后面可能跟一个乱码，然后是 CBA）
; ============================================

[org 0x7c00]        ; 告诉汇编器代码会被 BIOS 加载到 0x7c00

; ===== 设置 BIOS 显示功能 =====
mov ah, 0x0e        ; BIOS int 0x10, ah=0x0e = tty 模式（显示字符）

; ============================================
; 初始化栈
; ============================================
; 注意：栈底应该设在远离 0x7c00 的地方
;       避免覆盖我们的引导扇区代码（512 字节）
mov bp, 0x8000      ; 设置栈底（基址），远离引导扇区
mov sp, bp          ; 栈顶指向栈底（空栈状态）

; ============================================
; 压栈操作 - 演示栈向下生长
; ============================================
; 每次 push：
;   1. SP = SP - 2（栈向下移动 2 字节）
;   2. 将数据写入 SP 指向的地址

push 'A'            ; 栈: [A],     SP = 0x8000 - 2 = 0x7FFE
push 'B'            ; 栈: [A, B],  SP = 0x7FFE - 2 = 0x7FFC
push 'C'            ; 栈: [A, B, C], SP = 0x7FFC - 2 = 0x7FFA

; ============================================
; 直接访问栈内存 - 演示栈布局
; ============================================
; 栈内存布局：
;   0x8000  ← BP（栈底，未使用）
;   0x7FFE  ← 'A' 的位置（0x8000 - 2）
;   0x7FFC  ← 'B' 的位置（0x7FFE - 2）
;   0x7FFA  ← 'C' 的位置（0x7FFC - 2）← SP 指向这里

; 打印 0x7FFE 处的字符（第一个压入的 'A'）
mov al, [0x7ffe]    ; 0x8000 - 2 = 0x7ffe，直接访问栈内存
int 0x10            ; 打印 'A'

; 注意：不要尝试访问 [0x8000]
;       那是栈底位置，不是有效数据
;       访问会得到未定义的值
mov al, [0x8000]
int 0x10            ; 可能打印乱码（未定义行为）

; ============================================
; 出栈操作 - 演示 LIFO（后进先出）
; ============================================
; pop 操作：
;   1. 读取 SP 指向的值到寄存器
;   2. SP = SP + 2（栈向上收缩）
;
; 注意：x86 的 pop 只能按字（2 字节）操作
;       所以需要用 BX 作为中转，然后取低字节

pop bx              ; 弹出栈顶的 'C' 到 BX
mov al, bl          ; 只取 BL（低 8 位）
int 0x10            ; 打印 'C'

pop bx              ; 弹出现在的栈顶 'B' 到 BX
mov al, bl
int 0x10            ; 打印 'B'

pop bx              ; 弹出现在的栈顶 'A' 到 BX
mov al, bl
int 0x10            ; 打印 'A'

; ============================================
; 验证：已 pop 的数据变成垃圾
; ============================================
; 此时 SP 已经回到 0x8000
; 再次访问之前的栈位置会得到未定义的数据
mov al, [0x8000]
int 0x10            ; 打印垃圾数据（未定义）

; ===== 程序结束 =====
jmp $               ; 无限循环，程序停在这里

; ===== 引导扇区填充 =====
; 填充 0 直到 510 字节，然后写入引导签名 0xaa55
times 510-($-$$) db 0
dw 0xaa55
